public class Variable2 {
    public static void main(String[] args) {
        int level = 10;
        // 정수형 변수 level 선언과 동시에 값을 대입(초기화)

        int box = 20;
        // 정수형 변수 box 선언과 동시에 값을 대입(초기화)

        String str = "문자" ;// 한글자인 경우 '문' 으로 작성
        // 문자열형 변수 str 선언과 동시에 값을 대입(초기화)

        System.out.println(level);
        System.out.println(box);
        System.out.println(str);
    }
}
