public class IntegerEx1 {
    public static void main(String[] args) {
        // byte 자료형(1바이트)
        byte byteValue = 120;
        System.out.println("byte 값 : " + byteValue);

        // short 자료형(2바이트)
        short shortValue = 30000;
        System.out.println("short 값 : " + shortValue);

        // int 자료형(4바이트)
        int intValue = 2000000000;
        System.out.println("int 값 : " + intValue);

        // long 자료형(8바이트)
        long longValue = 9000000000L; // long 값을 입력할 때는 L을 붙여야 함
        System.out.println("long 값 : " + longValue);
    }
}
